def pierwiastek(x):
    import math
    z=math.sqrt(x)
    print(z)

def potega(x,y):
    import math
    z=math.pow(x,y)
    print(z)

def pierwiastek1(x,y):
    import math
    z=math.pow(x,-y)
    print(z)

def polekola(x):
    import math
    a=math.pow(x,2)
    z=a*math.pi
    print(z)

def silnia(y):
    import math
    z=math.factorial(y)
    print(z)

def floor(x):
    import math
    z=math.floor(x)
    print(z)

pierwiastek(3.7)
potega(3.7,4)
pierwiastek1(3.7,4)
polekola(3.7)
silnia(4)
floor(3.7)